package Basic.Lesson18;


//all of these are built in.
public class MathClass {
	public static void main(String[] args){
		
		//abs means how far away from zero
		System.out.println(Math.abs(-26.7));
		
		//ceil rounds any number up
		System.out.println(Math.ceil(7.4));
	
		//flor rounds any number down
		System.out.println(Math.floor(7.4));
		
		//will give you the maximum of the two numbers
		System.out.println(Math.max(10.5, 5.5));
		
		//will give you the minimum of two numbers
		System.out.println(Math.min(10.5 , 5.5));
		
		//puts any number to power this is 5^3 
		System.out.println(Math.pow(5, 3));
		
		//finds the square root of one number.
		System.out.println(Math.sqrt(16));
		
	}
	
}
